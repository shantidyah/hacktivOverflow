<template>
    <div class="modal fade" id="modal-edita" tabindex="-1" role="dialog" aria-labelledby="exampleModalLongTitle" aria-hidden="true">
		<div class="modal-dialog" role="document">
			<div class="modal-content">
				<div class="modal-header">
					<h5 class="modal-title" id="exampleModalLongTitle">Edit Answer</h5>
					<button type="button" class="close" data-dismiss="modal"  aria-label="Close">
							<span aria-hidden="true">&times;</span>
					</button>
				</div>
				<div class="modal-body">
					answer:
					<wysiwyg v-model="editAns" />					
				</div>
				<div class="modal-footer">
					<button type="button" class="btn btn-primary" data-dismiss="modal" @click="UpdateAns">Submit</button>
					<button type="button" class="btn btn-secondary" data-dismiss="modal" >Close</button>
				</div>
			</div>
		</div>
	</div>
</template>

<script>
import {mapState, mapActions} from 'vuex'

export default {
    name:'editans',
    computed:{
        ...mapState([
            'editAns'
        ]),
        editAns: {
            get(){
                return this.$store.state.editAns
            },
            set(value){
                return this.$store.commit('editAns',value)
            }
		},
    },
    methods:{
        ...mapActions([
            'UpdateAns'
        ])
    }

}
</script>

